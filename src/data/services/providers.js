const buildAvailability = (pattern) => {
  // pattern: array of 7 booleans starting today. Slot times follow.
  const today = new Date('2026-02-22T00:00:00Z'); // mock "today"
  const out = {};
  pattern.forEach((available, i) => {
    const d = new Date(today);
    d.setDate(today.getDate() + i);
    const key = d.toISOString().slice(0, 10);
    const baseSlots = ['09:00', '11:00', '14:00', '16:00', '18:00'];
    const slots = available
      ? baseSlots.map((time, idx) => ({ time, available: idx % 3 !== 1 }))
      : [];
    out[key] = { available, slots };
  });
  return out;
};

